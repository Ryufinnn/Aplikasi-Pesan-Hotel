      <h4>SEJARAH SINGKAT HOTEL PADANG</h4>
      </a> 
<div align="center">
		<p align="justify">Pada zaman kolonial Belanda sampai kemerdekaan, tersebutlah seorang pengusaha keturunan Tionghoa yang kaya raya. Konon kabarnya, beliau memliki kekayaan tersebar di Kota Padang, dan daerah-daerah di Sumbar sampai ke propinsi tetangga, bahkan sampai ke Jakarta. Tanah dan Bangunan milik beliau banyak sekali.        </p>
        <p align="justify">Salah satu bangunan milik beliau yang cukup besar dan ditempat uang strategi berada di jalan Bagindo Aziz Chan. Itulah bangunan yang menjadi cikal bakal Hotel Padang. Pada tahun 1960-an bangunan itu bukanlah berupa hotel, tepatnya boleh dikatakan sebagai kos-kosan atau kamar kontrakan. Pedagang, sopir, sales, dan mufasir yang kemalaman biasa menginap di sana. Tidak ada nama usaha, atau status hotel, hanya penginapan.</p>
        <p align="justify">Tahun berganti musim bertukar, ditahun 1970-an penginapan sederhana ini berkembang karena banyak peminat, maklum zaman itu belum banyak penginapan. Kamar yang dahulu nya hanya 10 kamar dengan dua kamar mandi dan WC. Dikembangkan dengan menambah 10 kamar dialokasikan belakang bangunan utama. Berselang 15 tahun berikutnya bangunan sudah mencapai 44 kamar. Statusnya sudah menjadi hotel dengan predikat Hotel Melati, namun masih dikelola dengan cara kovensional.</p>
		<p align="justify">Pada tahun 2000-an, hotel yang dulunya dikelola oleh sebuah yayasan, ditingkatkan menjadi Perseroan Terbatas(PT) bernama PT. Ang Rukun Sejahtera. Semua cucu Mr. Ang Ing Huat di plot sebagai pemegang saham. Itulah Hotel Padang yang dulunya hanya kos-kos an berkembang menjadi Hotel Bintang Satu dengan fasilitas 36 unit, restoran dan 2 meeting room.</p>
